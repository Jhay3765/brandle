export function getRandomRound(pool?: RoundData[]): RoundData {
  const src = pool ?? POOL;
  return src[Math.floor(Math.random() * src.length)];
}

/** Fisher–Yates shuffle (pure, returns a new array). */
function shuffle<T>(list: T[]): T[] {
  const a = [...list];
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

/** Get a full, shuffled copy of the pool (non-repeating pass). */
export function getShuffledRounds(pool?: RoundData[]): RoundData[] {
  const src = pool ?? POOL;
  return shuffle(src);
}
